public class JavaBasics5 {
    public static void main(String args[]){
        // Code to calculate sum => Single line comment
        int a = 10;
        int b = 5;
        int sum=a+b;
        System.out.println(sum);
    }    
}
/*
Multi line comment : 
a = 10 → value 10 is stored in memory, and a points to that location
b = 5 → value 5 is stored in another memory location, b points there
sum = a + b → CPU adds 10 + 5 = 15 and stores 15 in a new memory location for sum
So 👉 values are stored separately, and variable names just refer to their memory locations. 
*/

 